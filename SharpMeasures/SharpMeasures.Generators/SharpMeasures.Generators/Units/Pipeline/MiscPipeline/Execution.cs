namespace SharpMeasures.Generators.Units.Pipeline.MiscPipeline;

using Microsoft.CodeAnalysis;
using Microsoft.CodeAnalysis.Text;

using SharpMeasures.Generators.SourceBuilding;

using System;
using System.Text;

internal static class Execution
{
    public static void Execute(SourceProductionContext context, DataModel data)
    {
        string source = Composer.Compose(context, data);

        context.AddSource($"{data.Unit.Name}_Misc.g.cs", SourceText.From(source, Encoding.UTF8));
    }

    private class Composer
    {
        public static string Compose(SourceProductionContext context, DataModel data)
        {
            Composer composer = new(context, data);
            composer.Compose();
            return composer.Retrieve();
        }

        private SourceProductionContext Context { get; }
        private StringBuilder Builder { get; } = new();

        private DataModel Data { get; }

        private Composer(SourceProductionContext context, DataModel data)
        {
            Context = context;
            Data = data;
        }

        private void Compose()
        {
            StaticBuilding.AppendAutoGeneratedHeader(Builder);
            StaticBuilding.AppendNullableDirective(Builder);

            NamespaceBuilding.AppendNamespace(Builder, Data.Unit.Namespace);

            Builder.Append(Data.Unit.ComposeDeclaration());

            BlockBuilding.AppendBlock(Builder, ComposeTypeBlock, originalIndentationLevel: 0);
        }

        private string Retrieve()
        {
            return Builder.ToString();
        }

        private void ComposeTypeBlock(StringBuilder source, Indentation indentation)
        {
            if (Data.Biased)
            {
                ComposeBiasedTypeBlock(indentation);
            }
            else
            {
                ComposeUnbiasedTypeBlock(indentation);
            }
        }

        private void ComposeUnbiasedTypeBlock(Indentation indentation)
        {
            DocumentationBuilding.AppendDocumentation(Context, Builder, Data.Documentation, indentation, "Quantity");
            Builder.Append($"{indentation}public {Data.Quantity.FullyQualifiedName} {Data.Quantity.Name} {{ get; }}{Environment.NewLine}");

            DocumentationBuilding.AppendDocumentation(Context, Builder, Data.Documentation, indentation, "Constructor");
            BlockBuilding.AppendBlock(Builder,
                header: $"private {Data.Unit.Name}({Data.Quantity.FullyQualifiedName} {Data.Quantity.ParameterName})",
                blockContentAppender: constructorBlock,
                originalIndentationLevel: indentation.Level);

            void constructorBlock(StringBuilder source, Indentation indentation)
            {
                Builder.Append($"{indentation}{Data.Quantity.Name} = {Data.Quantity.ParameterName};");
            }

            Builder.Append(Environment.NewLine);
            DocumentationBuilding.AppendDocumentation(Context, Builder, Data.Documentation, indentation, "ScaledBy_Scalar");
            Builder.Append($"{indentation}public {Data.Unit.Name} ScaledBy(SharpMeasures.Scalar scale) => ScaledBy(scale.Value);{Environment.NewLine}");
            DocumentationBuilding.AppendDocumentation(Context, Builder, Data.Documentation, indentation, "ScaledBy_Double");
            Builder.Append($"{indentation}public {Data.Unit.Name} ScaledBy(double scale) => new({Data.Quantity.Name} * scale);{Environment.NewLine}");

            Builder.Append(Environment.NewLine);
            DocumentationBuilding.AppendDocumentation(Context, Builder, Data.Documentation, indentation, "WithPrefix_Metric");
            Builder.Append($"{indentation}public {Data.Unit.Name} WithPrefix(SharpMeasures.MetricPrefix prefix) => ScaledBy(prefix.Factor);{Environment.NewLine}");
            DocumentationBuilding.AppendDocumentation(Context, Builder, Data.Documentation, indentation, "WithPrefix_Binary");
            Builder.Append($"{indentation}public {Data.Unit.Name} WithPrefix(SharpMeasures.BinaryPrefix prefix) => ScaledBy(prefix.Factor);{Environment.NewLine}");

            Builder.Append(Environment.NewLine);
            DocumentationBuilding.AppendDocumentation(Context, Builder, Data.Documentation, indentation, "ToString");
            Builder.Append($"{indentation}public override string ToString() => $\"{{typeof({Data.Unit.Name})}}: {{{Data.Quantity.Name}}}\";{Environment.NewLine}");
        }

        private void ComposeBiasedTypeBlock(Indentation indentation)
        {
            DocumentationBuilding.AppendDocumentation(Context, Builder, Data.Documentation, indentation, "Quantity");
            Builder.Append($"{indentation}public {Data.Quantity.FullyQualifiedName} {Data.Quantity.Name} {{ get; }}{Environment.NewLine}");
            DocumentationBuilding.AppendDocumentation(Context, Builder, Data.Documentation, indentation, "Offset");
            Builder.Append($"{indentation}public Scalar Offset {{ get; }}{Environment.NewLine}");

            DocumentationBuilding.AppendDocumentation(Context, Builder, Data.Documentation, indentation, "Constructor");
            BlockBuilding.AppendBlock(Builder,
                header: $"private {Data.Unit.Name}({Data.Quantity.FullyQualifiedName} {Data.Quantity.ParameterName}, Scalar offset)",
                blockContentAppender: constructorBlock,
                originalIndentationLevel: indentation.Level);

            void constructorBlock(StringBuilder source, Indentation indentation)
            {
                source.Append($"{indentation}{Data.Quantity.Name} = {Data.Quantity.ParameterName};{Environment.NewLine}");
                source.Append($"{indentation}Offset = offset;");
            }

            Builder.Append(Environment.NewLine);
            DocumentationBuilding.AppendDocumentation(Context, Builder, Data.Documentation, indentation, "ScaledBy_Scalar");
            Builder.Append($"{indentation}public {Data.Unit.Name} ScaledBy(SharpMeasures.Scalar scale) => ScaledBy(scale.Value);{Environment.NewLine}");
            DocumentationBuilding.AppendDocumentation(Context, Builder, Data.Documentation, indentation, "ScaledBy_Double");
            Builder.Append($"{indentation}public {Data.Unit.Name} ScaledBy(double scale) => new({Data.Quantity.Name} * scale, Offset / scale);{Environment.NewLine}");

            Builder.Append(Environment.NewLine);
            DocumentationBuilding.AppendDocumentation(Context, Builder, Data.Documentation, indentation, "OffsetBy_Scalar");
            Builder.Append($"{indentation}public {Data.Unit.Name} OffsetBy(SharpMeasures.Scalar offset) => OffsetBy(offset.Value);{Environment.NewLine}");
            DocumentationBuilding.AppendDocumentation(Context, Builder, Data.Documentation, indentation, "OffsetBy_Double");
            Builder.Append($"{indentation}public {Data.Unit.Name} OffsetBy(double offset) => new({Data.Quantity.Name}, Offset + offset);{Environment.NewLine}");

            Builder.Append(Environment.NewLine);
            DocumentationBuilding.AppendDocumentation(Context, Builder, Data.Documentation, indentation, "WithPrefix_Metric");
            Builder.Append($"{indentation}public {Data.Unit.Name} WithPrefix(SharpMeasures.MetricPrefix prefix) => ScaledBy(prefix.Factor);{Environment.NewLine}");
            DocumentationBuilding.AppendDocumentation(Context, Builder, Data.Documentation, indentation, "WithPrefix_Binary");
            Builder.Append($"{indentation}public {Data.Unit.Name} WithPrefix(SharpMeasures.BinaryPrefix prefix) => ScaledBy(prefix.Factor);{Environment.NewLine}");

            Builder.Append(Environment.NewLine);
            DocumentationBuilding.AppendDocumentation(Context, Builder, Data.Documentation, indentation, "ToString");
            Builder.Append($"{indentation}public override string ToString() " +
                $"=> $\"{{typeof({Data.Unit.Name})}}: ({{{Data.Quantity.Name}}} + {{Offset}})\";{Environment.NewLine}");
        }
    }
}
