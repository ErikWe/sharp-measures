public class ComparisonTests
{
    [Theory]
    [ClassData(typeof(GenericDataset<EnergyDataset, EnergyDataset>))]
    public void Method(Energy lhs, Energy rhs)
    {
        Utility.QuantityTests.ComparisonTests.Method_ShouldMatchScalar(lhs, rhs);
