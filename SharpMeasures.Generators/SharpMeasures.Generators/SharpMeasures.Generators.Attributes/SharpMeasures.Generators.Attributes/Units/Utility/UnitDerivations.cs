namespace SharpMeasures.Generators.Units.Utility;

/// <summary>Some common expressions for unit derivation.</summary>
public static class UnitDerivations
{
    /// <summary>Multiplication of the two units.</summary>
    public const string Multiplication = "{0} * {1}";
    /// <summary>Division of the first unit by the second.</summary>
    public const string Division = "{0} / {1}";
}
